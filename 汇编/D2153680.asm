DATA SEGMENT 'DATA'
MSG DB "PLEASE INPUT A NUMBER: ",'$'
S1 DB 9,13,10,"Monday",13,10,'$'
S2 DB 9,13,10,"Tuesday",13,10,'$'
S3 DB 9,13,10,"Wednesday",13,10,'$'
S4 DB 9,13,10,"Thursday",13,10,'$'
S5 DB 9,13,10,"Friday",13,10,'$'
S6 DB 9,13,10,"Saturday",13,10,'$'
S7 DB 9,13,10,"Sunday",13,10,'$'
ERR DB 9,13,10,"Error!",13,10,'$'
ADDTBL DW F1,F2,F3,F4,F5,F6,F7
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA

START:MOV AX,DATA
MOV DS,AX
INPUT:LEA DX,MSG
MOV AH,9
INT 21H
MOV AH,1
INT 21H
CMP AL,'0'
JB ERROR
CMP AL,'7'
JA ERROR
CMP AL,'1'
JB EXIT

SUB AL,'1'
SHL AL,1
MOV BL,AL
MOV BH,0
JMP ADDTBL[BX]

F1:LEA DX,S1
JMP OUTPUT
F2:LEA DX,S2
JMP OUTPUT
F3:LEA DX,S3
JMP OUTPUT
F4:LEA DX,S4
JMP OUTPUT
F5:LEA DX,S5
JMP OUTPUT
F6:LEA DX,S6
JMP OUTPUT
F7:LEA DX,S7
JMP OUTPUT
ERROR:LEA DX,ERR
JMP OUTPUT

OUTPUT:
MOV AH,9
INT 21H
JMP INPUT

EXIT:
MOV AX,4C00H
INT 21H

CODE ENDS
END START